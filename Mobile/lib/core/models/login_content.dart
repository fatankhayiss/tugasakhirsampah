class LoginContent {
  final String headerImage;
  final String welcomeText;
  final String emailPlaceholder;
  final String passwordPlaceholder;
  final String forgotPasswordText;
  final String loginButtonText;
  final String noAccountText;
  final String registerLinkText;
  final String continueWithText;

  LoginContent({
    required this.headerImage,
    required this.welcomeText,
    required this.emailPlaceholder,
    required this.passwordPlaceholder,
    required this.forgotPasswordText,
    required this.loginButtonText,
    required this.noAccountText,
    required this.registerLinkText,
    required this.continueWithText,
  });
}
